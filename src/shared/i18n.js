function normalizeLanguage(language) {
  return SUPPORTED_LANGUAGES.includes(language) ? language : DEFAULT_LANGUAGE;
}

export function getCurrentLanguage() {
  const params = new URLSearchParams(window.location.search);
  const requestedLanguage = params.get('lang');

  if (requestedLanguage) {
    const language = normalizeLanguage(requestedLanguage);
    localStorage.setItem(STORAGE_KEY, language);
    return language;
  }

  return normalizeLanguage(localStorage.getItem(STORAGE_KEY) ?? DEFAULT_LANGUAGE);
}

export function setCurrentLanguage(language) {
  localStorage.setItem(STORAGE_KEY, normalizeLanguage(language));
}

export function t(key, replacements = {}) {
  const language = getCurrentLanguage();
  const text = translations[language]?.[key] ?? translations[DEFAULT_LANGUAGE]?.[key] ?? key;

  return Object.entries(replacements).reduce(
    (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
    text
  );
}

export function localizedCategoryName(category = {}) {
  const key = `category.${category.slug}`;
  const translated = t(key);
  return translated === key ? category.name : translated;
}

export function localizedCategoryDescription(category = {}) {
  const key = `categoryDesc.${category.slug}`;
  const translated = t(key);
  return translated === key ? (category.description || t('home.localOffers')) : translated;
}

export function localizedStatus(status = '') {
  const key = `status.${status}`;
  const translated = t(key);
  return translated === key ? status : translated;
}
